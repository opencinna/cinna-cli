"""Generate CLAUDE.md and BUILDING_AGENT.md from building context."""

import json
from pathlib import Path
from datetime import datetime, timezone

from cinna.config import CinnaConfig

CLAUDE_MD_TEMPLATE = """\
# Agent: {agent_name} — Local Development

> This file is auto-generated by cinna. Do not edit manually.
> Regenerated on: {timestamp}

## Building Context

Read `BUILDING_AGENT.md` for the full building mode system prompt — it contains the agent's role, development guidelines, available scripts, credentials, knowledge topics, and more. That file is the same prompt the cloud building agent receives.

## Local Development Environment

You are working in a **local development copy** of a remote agent. The workspace is mounted into a Docker container that replicates the agent's production runtime. Your goal is to develop and test the agent's scripts, prompts, and integrations — the same work that building mode does in the cloud.

### Key Rules

- **Use `cinna exec` to run scripts** (e.g., `cinna exec python scripts/main.py`). The container has the correct Python packages, system dependencies, and credentials. Running directly on the host may fail if dependencies differ.
- **DO NOT modify `CLAUDE.md` or `BUILDING_AGENT.md`** — both are auto-generated and will be overwritten on the next `cinna pull`.
- **Credentials** are in `workspace/credentials/credentials.json`. To update them, use the platform web UI and then run `cinna credentials`.
- **After making changes**, run `cinna push` to sync to the remote environment, then `cinna pull` to refresh the building context.

### How to Work

| Task | Command |
|------|---------|
| Run a script | `cinna exec python scripts/main.py` |
| Run any command in container | `cinna exec <command>` |
| Install a Python package | Add to `workspace/workspace_requirements.txt`, then `cinna rebuild` |
| Install a system package | Add to `workspace/workspace_system_packages.txt`, then `cinna rebuild` |
| Refresh credentials | `cinna credentials` |
| Push changes to remote | `cinna push` |
| Pull from remote + refresh context | `cinna pull` |
| Rebuild container | `cinna rebuild` |

### Workspace Structure

```
workspace/
  scripts/          — Agent Python scripts
  docs/             — Prompt files: WORKFLOW_PROMPT.md, ENTRYPOINT_PROMPT.md, REFINER_PROMPT.md
  webapp/           — HTML/CSS/JS dashboard + Python data endpoints
  files/            — Reports, CSVs, data files
  credentials/      — Integration credentials (pulled from platform)
  workspace_requirements.txt    — Python packages (pip install)
  workspace_system_packages.txt — System packages (apt-get install)
```

### MCP Tools

The following tools are available via MCP (auto-configured in `.mcp.json`):
{mcp_tools_section}
"""


def generate_context_files(
    building_context: dict,
    config: CinnaConfig,
    workspace_root: Path,
) -> None:
    """Generate CLAUDE.md and BUILDING_AGENT.md from building context response."""
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")

    # --- BUILDING_AGENT.md ---
    building_prompt = building_context.get("building_prompt", "")
    building_md = (
        f"> This file is auto-generated by cinna. Do not edit manually.\n"
        f"> Contains the building mode system prompt pulled from the platform.\n"
        f"> Regenerated on: {timestamp}\n\n"
        f"{building_prompt}"
    )
    (workspace_root / "BUILDING_AGENT.md").write_text(building_md)

    # --- CLAUDE.md ---
    mcp_tools = _format_mcp_tools(config)
    claude_md = CLAUDE_MD_TEMPLATE.format(
        agent_name=config.agent_name,
        timestamp=timestamp,
        mcp_tools_section=mcp_tools,
    )
    (workspace_root / "CLAUDE.md").write_text(claude_md)


def generate_mcp_json(config: CinnaConfig, workspace_root: Path) -> None:
    """Generate .mcp.json for Claude Code MCP tool discovery."""
    mcp_config = {
        "mcpServers": {
            "agent-knowledge": {
                "command": "cinna",
                "args": ["mcp-proxy"],
                "env": {
                    "CINNA_CONFIG": str(workspace_root / ".cinna" / "config.json"),
                },
            }
        }
    }
    (workspace_root / ".mcp.json").write_text(json.dumps(mcp_config, indent=2) + "\n")


def generate_opencode_json(config: CinnaConfig, workspace_root: Path) -> None:
    """Generate opencode.json for opencode MCP tool discovery."""
    opencode_config = {
        "mcp": {
            "agent-knowledge": {
                "type": "local",
                "command": ["cinna", "mcp-proxy"],
                "environment": {
                    "CINNA_CONFIG": str(workspace_root / ".cinna" / "config.json"),
                },
                "enabled": True,
            }
        }
    }
    (workspace_root / "opencode.json").write_text(
        json.dumps(opencode_config, indent=2) + "\n"
    )


def generate_gitignore(workspace_root: Path) -> None:
    """Generate .gitignore for the local dev workspace."""
    content = """\
.cinna/
.claude/settings.local.json
CLAUDE.md
BUILDING_AGENT.md
.mcp.json
opencode.json
cinna.log
workspace/credentials/
workspace/__pycache__/
workspace/*.pyc
"""
    gitignore = workspace_root / ".gitignore"
    if not gitignore.exists():
        gitignore.write_text(content)


def _format_mcp_tools(config: CinnaConfig) -> str:
    """Format MCP tools list for CLAUDE.md."""
    tools = [
        "- `knowledge_query` — Search the agent's knowledge base for documentation"
    ]
    if config.knowledge_sources:
        topics = ", ".join(t for ks in config.knowledge_sources for t in ks.topics)
        if topics:
            tools.append(f"  Available topics: {topics}")
    return "\n".join(tools)
